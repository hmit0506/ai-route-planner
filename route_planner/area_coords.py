def get_area_center(area: str) -> tuple[float, float] | None:
    if not area:
        return None
    if area in AREA_COORDS:
        return AREA_COORDS[area]
    for k, v in AREA_COORDS.items():
        if k in area or area in k:
            return v
    return None
